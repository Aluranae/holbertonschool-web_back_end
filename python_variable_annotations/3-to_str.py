#!/usr/bin/env python3
"""
Module 3-to_str
Ce module contient une fonction qui retourne
la représentation en chaîne de caractères d'un nombre flottant.
"""


def to_str(n: float) -> str:
    """
    Convert a floating-point number to its string representation.

    Args:
        n (float): The number to convert.

    Returns:
        str: The string version of the float number.
    """
    return str(n)
